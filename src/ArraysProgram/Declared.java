package ArraysProgram;

class Declared {
	public static void main(String args[]) {
		// declaration, instantiation and
		// initialization 
		int a[] = { 33, 3, 4, 5 };
		// printing array
		for (int i = 0; i < a.length; i++)
		{
			// length is the property of array
			System.out.println(a[i]);
		}
	}
}